// Boolean is used to store two possible values. Either true or false.
// Type casting is not possible in boolean data types.
// Default value of boolean data type is : False.
// Default value is 1 bit.
package datatypesinjava;

public class BooleanDataTypes {

	public static void main(String[] args) {
		
		boolean a = true;
		boolean b = false;
		boolean c = a&b;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);  // because the default value of boolean is false
	}
}
