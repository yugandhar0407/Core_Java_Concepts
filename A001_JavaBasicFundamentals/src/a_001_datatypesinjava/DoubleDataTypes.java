// The double data type is a double precision 64-bit 
// Its value range is Unlimited
// Its has high precision than float(15 decimal)
// default value is 0.0d
// default size is 8 byte/64 bit
// For decimal values, double data is mostly use.
// it must end with d
package datatypesinjava;

public class DoubleDataTypes {

	public static void main(String[] args) {

		double d = 10d / 6d;
		
		System.out.println(d);
	}

}
