// Float data type is used to store fractional numbers
// It has a 7 decimal digit precision in memory
// default value - 0.0f
// default size - 4 bytes/32 bits
// it must end with f

package datatypesinjava;

public class FloatDataTypes {

	public static void main(String[] args) {
		
		float a = 10f;
		
		float b = 25f;
		
		System.out.println(a/b);
		
		System.out.println();
		
		float c = 10f/6f;
		System.out.println(c);

	}

}
