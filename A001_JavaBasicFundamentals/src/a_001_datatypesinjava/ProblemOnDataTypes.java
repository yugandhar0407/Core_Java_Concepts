package datatypesinjava;

public class ProblemOnDataTypes {
	
	public static void main(String[] args) {
		
		int a = 1;
		float b = 2f;
		double c = 3d;
		long d = 5L;
		byte e = 127;
		
		System.out.println(c/b);
		System.out.println("****************");
		System.out.println(b/a);
		System.out.println("****************");
		System.out.println(c/a);
		System.out.println("****************");
		System.out.println(a/d);
		System.out.println("****************");
		System.out.println(a/e);
		
		
		
	}

}
