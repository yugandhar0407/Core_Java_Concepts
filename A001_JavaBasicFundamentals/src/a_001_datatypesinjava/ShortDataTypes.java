// The short data type is a 16-bit two complement integer.
// Its value range lies between -32768 to 32768
// default value = 0
// size = 2 byte /16 bit


package datatypesinjava;

public class ShortDataTypes {

	public static void main(String[] args) {
		
		short a = 123;
		
		short b = 321;
		
		short c = (short)(a+b);
		
		System.out.println(c);

	}

}
