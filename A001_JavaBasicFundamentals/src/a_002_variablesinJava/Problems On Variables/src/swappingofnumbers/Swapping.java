package swappingofnumbers;

public class Swapping {

	public static void main(String[] args) {
		
		int a = 120;
		int b = 140;
		
		
		int c = a;
		a=b;
		b=c; //with third variable
		
		System.out.println(a);
		System.out.println(b);
		
		System.out.println();
		
		
		int x = 111;
		int y = 222;
		
		int z = x;
		x = y;
		y= z;
		
		System.out.println(x);
		System.out.println(y);
		
	}

}
