package operatorsInJava;

public class ArithmeticOperators {

	public static void main(String[] args) {

		int a = 1;
		int b = 2;
		int c = 3;
		int d = 4;

		System.out.println(a + b + c + d);
		System.out.println(a - b - c - d);
		System.out.println(a / b);
		System.out.println(a * b * c * d);
		System.out.println("************");
		int a1 = 15;

		while (a1 <= 15) {
			System.out.println("A1 is not greater than 15");
			a1++;
		}

	}

}
