package loopsinjava;

public class DoWhileLoopExamples {

	public static void main(String[] args) {
		
		int j = 20;
		do {
			System.out.println(j);
			j++;
		} while (j <= 10);
		
		System.out.println();
		
		//Infinite Do While Loop
		/*
		 * do { System.out.println("This is the infinite do while loop"); }while(true);
		 */
	}

}
