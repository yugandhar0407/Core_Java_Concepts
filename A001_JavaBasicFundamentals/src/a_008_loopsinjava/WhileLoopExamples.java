package loopsinjava;

public class WhileLoopExamples {

	public static void main(String[] args) {
		
		int i = 1;
		while (i <= 10) {
			System.out.println(i);
			i++;
		}
		System.out.println();
		
		/*
		 * //Infinite While Loop 
		 * while(true) { 
		 * System.out.println("This is the Infinite While Loop");
		 * }
		 */
	}

}
