package methodsInJava;

public class PredefinedMethods {

	public static void main(String[] args) {
		
		System.out.println("The Sum of the two numbers: "+Math.addExact(5,5));
		
		System.out.println("The Maximum Number is: "+Math.max(9, 8));
		
		System.out.println("The Minimum Number is: "+Math.min(9, 8));
		
		System.out.println("The Increment Number is: "+Math.incrementExact(5));
		
		System.out.println("The Division of the two numbers: "+Math.subtractExact(5, 5));
	}

}
